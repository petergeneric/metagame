const jitter = (coordinates) => {
	if (!coordinates) return undefined;

	coordinates = [...coordinates];

	// TODO take a copy of coordinates (x,y) and randomly jitter them by +/- 3 meters
	const ONE_METER_IN_DEGREES = 0.000009;

	coordinates[0] += (Math.random() * (ONE_METER_IN_DEGREES * 6)) - (ONE_METER_IN_DEGREES * 3);
	coordinates[1] += (Math.random() * (ONE_METER_IN_DEGREES * 6)) - (ONE_METER_IN_DEGREES * 3);

	return coordinates;
}


